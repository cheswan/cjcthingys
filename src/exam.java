    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try{
      DB_connection.init();

        Connection c = DB_connection.getConnection();
        PreparedStatement ps = c.prepareStatement("SELECT * FROM medical_information");
        String query = "SELECT Ailment_Disease, Classification, Description, Cure FROM medical_information WHERE Ailment_Disease LIKE '%" + jTextPane1.getText() + "%'OR Classification LIKE '%" + jTextPane1.getText() + "%' "
                + "OR Description LIKE '%" + jTextPane1.getText() + "%' OR Cure LIKE '%" + jTextPane1.getText() + "%'";
      rs = ps.executeQuery(query);
      jTable1.setModel(DbUtils.resultSetToTableModel(rs)); 

    c.close();

}
     catch (Exception e) { 
        JOptionPane.showMessageDialog(null,"Got an exception!");
        System.err.println(e.getMessage()); 
    }        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
    try{
        int row=jTable1.getSelectedRow();
        String Table_click=(jTable1.getModel().getValueAt(row,0).toString());
        String sql ="select * from medical_information where Ailment_Disease='"+Table_click+"'";
        ps=c.prepareStatement(sql);
        rs=ps.executeQuery();
        if (rs.next()){
            String add1 =rs.getString("Ailment_Disease");
            Ailments.setText(add1);
            String add2 =rs.getString("Classification");
            Classification.setText(add2);
            String add3 =rs.getString("Description");
            Description.setText(add3);
            String add4 =rs.getString("Cure");
            Cure.setText(add4);
        }
    }catch (Exception e){
        JOptionPane.showMessageDialog(null, e);
    }
    }//GEN-LAST:event_jTable1MouseClicked

